package com.monitortracker;

public class grstruct 
{
  public String name;
  public String gpsdata;
  public String stime;
  public String dtime;
  
  public grstruct(String cname, String cgps, String sdtime, String ddtime)
  {
    name = cname;
    gpsdata = cgps;
    stime = sdtime;
    dtime = ddtime;
  }
  
   
}